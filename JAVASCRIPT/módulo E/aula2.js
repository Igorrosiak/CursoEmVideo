/* FOR */

/* Comparando com o while da aula passada */

var c=1
while(c<=10){
    console.log(`Linha ${c}`)
    c++
}

/* for(inicio; teste; incremento) */
for(var c=1; c<=10; c++){
    console.log(`Linha ${c}`)
}

